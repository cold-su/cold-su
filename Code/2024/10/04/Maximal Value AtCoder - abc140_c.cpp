const bool HAVE_MULTIPLE_TESTCASES = false;

void solve() {
    int n;
    std::cin >> n;

    int sum = 0, curr = INT_MAX;
    while (--n) {
        int a;
        std::cin >> a;

        sum += std::min(a, curr);
        curr = a;
    }
    std::cout << sum + curr;
}

