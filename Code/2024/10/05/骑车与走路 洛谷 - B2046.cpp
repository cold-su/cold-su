const bool HAVE_MULTIPLE_TESTCASES = false;

void solve() {
    int n;
    std::cin >> n;

    double a = n / (double)3.0 + 50;
    double b = n / (double)1.2;

    if (a == b) {
        std::cout << "All";
        return;
    }
    std::cout << (a < b ? "Bike" : "Walk");
}

