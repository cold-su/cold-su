const bool HAVE_MULTIPLE_TESTCASES = false;

void solve() {
    int n, r;
    std::cin >> n >> r;

    int ans = 0;

    for (int i = 0; i < n; i++) {
        int a, b;
        std::cin >> a >> b;
        if (r >= b)
            ans = std::max(ans, a);
    }

    std::cout << ans;
}

signed main() {
    if (HAVE_MULTIPLE_TESTCASES) {
        int t;
        std::cin >> t;
        while (t--) { solve(); }
    } else solve();
    return 0;
}