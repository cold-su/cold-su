const bool MULTIPLE_TESTCASES = false;

void solve() {
    int a, b, n;
    std::cin >> a >> b >> n;

    bool is = false;
    while (n > 0) {
        is = !is;
        n -= std::gcd((is ? a : b), n);
    }
    std::cout << !is;
}

signed main() {
    if (MULTIPLE_TESTCASES) {
        int t;
        std::cin >> t;
        while (t--) { solve(); }
    } else solve();
    return 0;
}