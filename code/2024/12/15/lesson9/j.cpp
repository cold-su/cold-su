const bool HAVE_MULTIPLE_TESTCASES = false;
#define int long long int

void solve() {
	std::string n;
	std::cin >> n;

	int sum = 0;
	for (char i : n) {
		sum += i - '0';
	}

	std::cout << std::fixed << std::setprecision(3);
	std::cout << sqrt(sum);
}

signed main() {
	std::ios::sync_with_stdio(0);
	std::cin.tie(0);
	if (HAVE_MULTIPLE_TESTCASES) {
		int t;
		std::cin >> t;
		while (t--)
			solve();
	} else solve();
	return 0;
}
